  /**
   * Returns translation strings for the given language code, falling back to English.
   * @param {string} langCode
   * @returns {Object}
   */
  function getTranslations(langCode) {
    const code = typeof langCode === 'string'
      ? langCode.toLowerCase().split('-')[0].split('_')[0]
      : 'en';
    return Object.prototype.hasOwnProperty.call(translations, code)
      ? translations[code]
      : translations['en'];
  }

  /**
   * Applies translations to all [data-i18n] elements in the current document.
   * @param {string} langCode
   */
  function applyTranslations(langCode) {
    const t = getTranslations(langCode);
    const nodes = document.querySelectorAll('[data-i18n]');
    for (const node of nodes) {
      const key = node.dataset.i18n;
      if (Object.prototype.hasOwnProperty.call(t, key)) {
        if (node.tagName === 'INPUT' && (node.type === 'text' || node.type === 'search')) {
          node.placeholder = t[key];
        } else {
          node.textContent = t[key];
        }
      }
    }
    // Update placeholder attributes separately
    const placeholderNodes = document.querySelectorAll('[data-i18n-placeholder]');
    for (const node of placeholderNodes) {
      const key = node.dataset.i18nPlaceholder;
      if (Object.prototype.hasOwnProperty.call(t, key)) {
        node.placeholder = t[key];
      }
    }
  }

  window.LingoStreamI18n = { getTranslations, applyTranslations };
